def add_two_integers(int_1: int, int_2: int) -> int:
    """
    Function that takes two integers and add them up
    """
    int_out = int_1 + int_2
    return int_out


def multiply_two_floats(flt_1: float, flt_2: float) -> float:
    """
    Function that multiply two floats
    """
    flt_out = flt_1*flt_2

    return flt_out

    
